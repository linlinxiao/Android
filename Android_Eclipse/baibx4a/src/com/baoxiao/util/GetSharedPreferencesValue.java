package com.baoxiao.util;

import android.content.Context;
import android.content.SharedPreferences;

public class GetSharedPreferencesValue {
	
	public static String getUserId(Context context){
		String userid = context.getSharedPreferences("loading", 0).getString(
				"userid", null);
		return userid;
	}
	public static String getType(Context context){
		String userid = context.getSharedPreferences("loading", 0).getString(
				"type", null);
		return userid;
	}
	public static String getInsurerperson(Context context){
		String userid = context.getSharedPreferences("loading", 0).getString(
				"insurerperson", null);
		return userid;
	}
	public static String getUpdateTishi(Context context){
		String userid = context.getSharedPreferences("loading", 0).getString(
				"updateTishi", null);
		return userid;
	}
	public static String getAutoUpdate(Context context){
		String userid = context.getSharedPreferences("loading", 0).getString(
				"autoUpdate", null);
		return userid;
	}

}
